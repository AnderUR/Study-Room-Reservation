<ul>
    <li>
        Library study rooms are reserved for group study for academic purposes only by
        current
        City
        Tech students or students with faculty/staff.<br>
    </li>
    <li>
        Reservations can be made at least 1 day in advance, and will be cancelled if a group
        arrives more than 15 minutes past their scheduled time.<br>
    </li>
    <li>
        Study rooms must be occupied by 3 to 5 students for up to 3 hours. There is a limit of 1
        use per room per day for any group.<br>
    </li>
    <li>
        To use a study room, ALL students in the group must present their City Tech IDs at
        the Borrow & Return Desk. <br>
    </li>
    <li>
        The study room key must be returned to the Borrow & Return Desk on time to avoid
        late fees. The overdue fine for returning the key late is 2 cents per minute, with a
        maximum fine of $10.<br>
    </li>
    <li>
        When leaving the study room, the group must ensure the room is clean, the equipment
        is functioning, furniture is in order, and the door is locked.<br>
    </li>
    <li>
        Absolutely no eating or drinking is allowed in the study rooms.
    </li>
</ul>